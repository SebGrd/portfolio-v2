            <p><span>Qu’est-ce que le BTS SIO ?</span></p>
            <p>Le BTS <b>Services informatique aux Organisations</b> est un diplôme reconnu par l’état de niveau <b>Bac+2</b>.
                Il remplace l’ancien BTS informatique de Gestion (BTS IG). Il propose deux spécialités bien
                distinctes : le BTS SIO <b>SISR</b> et le BTS SIO <b>SLAM</b>.
            </p>
            <p>Le BTS SIO option <b>SLAM (Solutions Logicielles et Applications Métiers)</b> forme des
                <b>spécialistes des logiciels</b> (rédaction
                d’un cahier des charges, formulation des besoins et spécifications,
                développement, intégration au sein de la société).
                <br>
                Les techniciens supérieurs en informatique option slam, sont
                <b>préparés aux métiers de</b> :
            </p>
